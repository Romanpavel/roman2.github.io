////////////////////////////////////////////////////////////////////////////////

function min(a, b) { // creat 2 parameters

// if a is smaller than b return a
  if (a < b) {
    return a;
    
    // otherwise return b
  } else {
    return b;
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) { // create a number parameter

// create a base case where n is equal to 0, o is even, return true if base case is reached
  if(n === 0) {
    return true;
    
    // second base case for odd numbers, return false once reached
  } else if (n === 1) {
    return false;
    
    // edge case, if the number is smaller than 0, use recorsion to flip the value 
  } else if (n < 0) {
    return isEven(-n);
  }
  
  // using recursion subtract 2 from n parameter, until either the 0 or 1 base case is reached
  return isEven(n - 2);

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  
  // create a variable to keep track of the count
  
  var count = 0;
  
  // loop over the string
  
  for (let i = 0; i < string.length; i++) {
    
    // if the element matches the character parameter
    
    if(string[i] === char) {
      
      // increase the count by every instance
      
      count++;
    }
  }
  
  // return the count
  
  return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  
  // using the countChars function, return looking for 'B'
  
  return countChars(string, 'B');

}

